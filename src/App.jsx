function answerKanaToIndex(k) {
  const idx = kana.indexOf(k);
  return idx >= 0 ? idx : 0;
}

function nowIso() {
  return new Date().toISOString();
}

function clamp(n, min, max) {
  return Math.max(min, Math.min(max, n));
}

function computeModeLabel(mode) {
  if (mode === "all") return "すべての問題";
  if (mode === "wrong") return "前回不正解の問題のみ";
  if (mode === "review") return "要復習の問題のみ";
  return "すべての問題";
}

function buildSessionOrder(questions, mode, progress) {
  const list = Array.isArray(questions) ? questions : [];
  const p = typeof progress === "object" && progress ? progress : {};
  const ids = list.map((q) => q.id);
  if (mode === "wrong") {
    const filtered = ids.filter((id) => p?.[id]?.lastResult === false);
    return filtered.length ? filtered : [];
  }
  if (mode === "review") {
    const filtered = ids.filter((id) => p?.[id]?.review === true);
    return filtered.length ? filtered : [];
  }
  return ids;
}

function formatDate(iso) {
  try {
    if (!iso) return "—";
    const d = new Date(iso);
    if (Number.isNaN(d.getTime())) return "—";
    return d.toLocaleString();
  } catch {
    return "—";
  }
}

