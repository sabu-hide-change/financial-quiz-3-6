const LS_KEY = "smart-3-6-ops:v1";

function safeJsonParse(raw, fallback) {
  try {
    if (raw == null) return fallback;
    const parsed = JSON.parse(raw);
    return parsed ?? fallback;
  } catch (e) {
    console.log("[LS] parse failed -> fallback", e);
    return fallback;
  }
}

function loadPersisted() {
  try {
    const raw = localStorage.getItem(LS_KEY);
    const data = safeJsonParse(raw, {});
    const progress = typeof data === "object" && data ? data.progress : {};
    const ui = typeof data === "object" && data ? data.ui : {};
    const normalized = {
      progress: typeof progress === "object" && progress ? progress : {},
      ui: typeof ui === "object" && ui ? ui : {},
    };
    console.log("[LS] loaded", normalized);
    return normalized;
  } catch (e) {
    console.log("[LS] load failed -> defaults", e);
    return { progress: {}, ui: {} };
  }
}

function savePersisted(next) {
  try {
    localStorage.setItem(LS_KEY, JSON.stringify(next));
    console.log("[LS] saved", next);
  } catch (e) {
    console.log("[LS] save failed", e);
  }
}

