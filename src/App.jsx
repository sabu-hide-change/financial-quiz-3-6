function Explainer({ blocks }) {
  const normalized = Array.isArray(blocks) ? blocks : [];
  return (
    <div className="space-y-4">
      {normalized.map((b, idx) => {
        if (!b || typeof b !== "object") return null;
        if (b.type === "p") {
          return (
            <p
              key={idx}
              className="text-sm leading-7 text-slate-800 whitespace-pre-wrap"
            >
              {b.text}
            </p>
          );
        }
        if (b.type === "h") {
          return (
            <div key={idx} className="text-sm font-bold text-slate-900">
              {b.text}
            </div>
          );
        }
        if (b.type === "ul") {
          return (
            <ul
              key={idx}
              className="list-disc pl-5 space-y-2 text-sm text-slate-800"
            >
              {(b.items ?? []).map((it, j) => (
                <li key={j} className="whitespace-pre-wrap">
                  {it}
                </li>
              ))}
            </ul>
          );
        }
        if (b.type === "table") {
          return (
            <MiniTable
              key={idx}
              headers={b.headers ?? []}
              rows={b.rows ?? []}
              caption={b.caption ?? ""}
            />
          );
        }
        if (b.type === "scmFlow") {
          const nodes = Array.isArray(b.nodes) ? b.nodes : [];
          return (
            <div key={idx} className="rounded-2xl border border-slate-200 bg-white p-4">
              <div className="text-sm font-semibold text-slate-900 mb-3">
                {b.caption ?? "SCM イメージ（図の代替表現）"}
              </div>
              <div className="grid gap-3 md:grid-cols-5">
                {nodes.map((n) => (
                  <FlowChip key={n.title} title={n.title} subtitle={n.subtitle} />
                ))}
              </div>
              <div className="mt-3 flex items-center gap-2 text-xs text-slate-600">
                <ChevronRight className="h-4 w-4" />
                <span className="whitespace-pre-wrap">
                  {b.note ??
                    "モノの流れ（左→右）と、需要/販売/生産などの情報共有（双方向）を、レイアウトで再現しています。"}
                </span>
              </div>
            </div>
          );
        }
        return null;
      })}
    </div>
  );
}

